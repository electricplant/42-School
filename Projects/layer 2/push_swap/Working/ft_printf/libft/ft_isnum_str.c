#include "libft.h"

int	ft_isnum_str(char *str)
{
	int	i;
	i = 0;

	if (str[0] == '-')
		i = 1;
	while (str[i] != '\0')
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}